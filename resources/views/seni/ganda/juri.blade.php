    <script>
        // Score tracking
        let scores = {
            teknik: 0,
            kekuatan: 0,
            penampilan: 0
        };

        // Get parameters from URL
        function getUrlParameter(name) {
            const urlParams = new URLSearchParams(window.location.search);
            return urlParams.get(name);
        }

        // Get user_id from URL path
        function getUserId() {
            const pathParts = window.location.pathname.split('/');
            return pathParts[pathParts.length - 1] || null;
        }

        // Get match_id from page data (passed from controller)
        function getMatchId() {
            // This will be populated by the controller via blade template
            return {{ $id ?? 1 }};
        }

        // Generate score buttons for each category
        function generateButtons(containerId, category) {
            const container = document.getElementById(containerId);
            container.innerHTML = '';
            
            for (let i = 1; i <= 30; i++) {
                const value = (i * 0.01).toFixed(2);
                const button = document.createElement('button');
                button.className = 'w-8 h-8 border border-gray-300 rounded text-xs font-medium hover:bg-blue-100 hover:border-blue-300 transition-colors focus:outline-none';
                button.textContent = value;
                button.onclick = () => selectScore(category, parseFloat(value), button, containerId);
                container.appendChild(button);
            }
        }

        // Select score function
        function selectScore(category, value, selectedButton, containerId) {
            // Remove active state from all buttons in this category
            const container = document.getElementById(containerId);
            const buttons = container.querySelectorAll('button');
            buttons.forEach(btn => {
                btn.className = 'w-8 h-8 border border-gray-300 rounded text-xs font-medium hover:bg-blue-100 hover:border-blue-300 transition-colors focus:outline-none';
            });

            // Add active state to selected button
            selectedButton.className = 'w-8 h-8 border-2 border-blue-600 bg-blue-600 text-white rounded text-xs font-bold transition-colors focus:outline-none';

            // Update score
            scores[category] = value;
            document.getElementById(category + '-score').textContent = value.toFixed(2);
            
            // Update total
            updateTotal();
        }

        // Update total score
        function updateTotal() {
            const total = 9.10 + scores.teknik + scores.kekuatan + scores.penampilan;
            document.getElementById('total-score').textContent = total.toFixed(2);
        }

        // Submit scores via AJAX
        function submitScores() {
            const userId = getUserId();
            const matchId = getMatchId();
            
            if (!userId) {
                alert('User ID tidak ditemukan!');
                return;
            }

            const data = {
                user_id: parseInt(userId),
                pertandingan_id: parseInt(matchId),
                teknik: scores.teknik,
                kekuatan: scores.kekuatan,
                penampilan: scores.penampilan
            };

            // Show loading
            const submitBtn = event.target;
            submitBtn.disabled = true;
            submitBtn.textContent = 'Mengirim...';

            fetch('/juri-seni-ganda/kirim-poin', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': '{{ csrf_token() }}'
                },
                body: JSON.stringify(data)
            })
            .then(response => response.json())
            .then(result => {
                if (result.status === 'success') {
                    const total = 9.10 + scores.teknik + scores.kekuatan + scores.penampilan;
                    alert(`✅ Penilaian berhasil dikirim!\n\nUser ID: ${userId}\nTeknik Dasar: ${scores.teknik.toFixed(2)}\nKekuatan & Kecepatan: ${scores.kekuatan.toFixed(2)}\nPenampilan & Gaya: ${scores.penampilan.toFixed(2)}\n\nTotal Score: ${total.toFixed(2)}`);
                } else {
                    alert('❌ Gagal mengirim penilaian: ' + (result.message || 'Unknown error'));
                }
            })
            .catch(error => {
                console.error('Error:', error);
                alert('❌ Terjadi kesalahan saat mengirim penilaian!');
            })
            .finally(() => {
                submitBtn.disabled = false;
                submitBtn.textContent = 'Submit Penilaian';
            });
        }

        // Initialize buttons on page load
        document.addEventListener('DOMContentLoaded', function() {
            generateButtons('teknik-buttons', 'teknik');
            generateButtons('kekuatan-buttons', 'kekuatan');
            generateButtons('penampilan-buttons', 'penampilan');
            
            // Show current user ID
            const userId = getUserId();
            if (userId) {
                console.log('User ID:', userId);
                console.log('Match ID:', getMatchId());
            }
        });
